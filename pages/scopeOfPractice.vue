<h2>Guidance for the Scope of Practice </h2>
<p><a name="principle-1"></a><strong>SOP Principle 1 — Follow the <a href="https://devteam.yogaalliance.org/AboutYA/OurPolicies/CodeofConduct" target="_blank">Yoga Alliance™ Code of Conduct</a> </strong>
</p>
<p>SOP Principle 1 requires Members to follow the <strong>Yoga Alliance Code of Conduct</strong>, which consists of the following eight principles:
</p>
<ol>
	<li>Adhere to applicable law.
	</li>
	<li>Follow the Yoga Alliance Scope of Practice.
	</li>
	<li>Follow the <a href="https://devteam.yogaalliance.org/About_Us/Policies/AntiHarassmentPolicy" target="_blank">Yoga Alliance Anti-Harassment Policy</a>.
	</li>
	<li>Follow the <a href="https://devteam.yogaalliance.org/About_Us/Policies/Policy_Prohibiting_Sexual_Misconduct" target="_blank">Yoga Alliance Sexual Misconduct Policy</a>.
	</li>
	<li>Do no harm.
	</li>
	<li>Actively include all individuals.
	</li>
	<li>Respect Student-Teacher relationships.
	</li>
	<li>Maintain honesty in communications.
	</li>
</ol>
<br>
<p><a name="principle-2"></a><strong>SOP Principle 2 — Teach Yoga</strong>
</p>
<p>SOP Principle 2 allows Members to <strong>teach yoga</strong> and to offer instruction and education on yoga practices and principles that responsibly reflect the level of yoga education, training, and experience of both the Members and the <a href="https://devteam.yogaalliance.org/About_Us/Policies/Definition_of_Key_Terms#student" target="_blank">Student(s)</a>. Members may teach yoga in a group, in a one-on-one setting, or online.
</p>
<p>Members must limit teaching to practices and learnings that align with yoga philosophy and the&nbsp;lineage, style, and methodology  for which the Member is qualified and in accordance with the competencies described in the <a href="https://devteam.yogaalliance.org/New_RYS_Standards/Common_Core_Curriculum_for_RYS_200" target="_blank">Yoga Alliance Common Core Curriculum Standards</a>.
</p>
<br>
<p><a name="principle-3"></a><strong>SOP Principle 3 — Adjust Posture or Practice with <a href="https://devteam.yogaalliance.org/About_Us/Policies/Definition_of_Key_Terms#explicit-consent" target="_blank">Explicit and Informed Consent</a> </strong>
</p>
<p>SOP Principle 3 allows the Member to <strong>adjust posture or practices only with Explicit and Informed Consent</strong>.  All physical adjustments of any type are intentionally limited to situations where there has been prior Explicit and Informed Consent.  Previous consent does not imply future permission or allow for future physical adjustment or contact.
</p>
<p>“Explicit and Informed Consent” is narrowly defined as overt permission for a specific Teacher to use hands-on contact that has been granted by the Student. This permission can be given verbally, in writing, by an unambiguous gesture, or via a consent indicator. Silence or lack of resistance, in and of itself, does not demonstrate consent.
</p>
<br>
<p><a name="principle-4"></a><strong>SOP Principle 4 —  Share and Cite Yogic Philosophy, History, and Anatomy </strong>
</p>
<p>SOP Principle 4 requires Members to properly cite sources when providing instruction in yogic philosophy, history, and anatomy.  This may include teachings, studies, and the Member’s own personal commentary or opinion from many sources, including those listed below. In conformance with the Yoga Alliance&nbsp;Code of Conduct, Members must accurately disclose the source of the information, commentary, or opinion.
</p>
<ol type="a">
	<li>Yoga texts
	</li>
	<li>The teachings of direct yoga Teachers of the Member
	</li>
	<li>The teachings of specific lineage traditions within a historical or contemporary yoga tradition
	</li>
	<li>The work of degreed scholars of Religious Studies, Indology, Sanskrit, etc.
	</li>
	<li>The work of degreed scholars of Anatomy, Physiology, etc.
	</li>
</ol>
<br>
<p><a name="principle-5"></a><strong>SOP Principle 5 — Advise and Teach Within Permitted Scope</strong>
</p>
<p>SOP Principle 5 <strong>prohibits members from advising or teaching in areas where the Member does not have appropriate credentials and competence</strong>.  The goal of Principle 5 is to provide guidance in cases where Members may encounter situations beyond the scope of their credentials, experience, or abilities.
</p>
<ol type="a">
	<li>Members must not provide advice or services for which they are not properly and currently licensed, during any program, including residential or immersion.  Members may, at their own discretion, provide referral lists of locally available resources for Students, including but not limited to: medical and mental health professionals, law   enforcement, suicide prevention hotline, sexual abuse hotline, and Yoga Alliance.</li>
	<li>Unless the Member is both competent and properly credentialed in the specific field in which they seek to provide services, advice, or products, the Member must (1) gain competence and proper credentials, (2) engage the services of or refer the Student to a competent and properly credentialed professional, or (3) decline to provide such service or advice.  </li>
	<li>Yoga Alliance&nbsp;credentials must never be used to imply competency or to promote the Member in fields not specifically included in the Yoga Alliance Scope of Practice.</li>
</ol>
<br>
<p><a name="principle-6"></a><strong>SOP Principle 6 — Maintain Relevant Credentials</strong>
</p>
<p>SOP Principle 6 requires Members to <strong>maintain relevant credentials</strong> including compliance with any registration or continuing education requirements.  Members must:
</p>
<ol type="a">
	<li><strong>Maintain and comply</strong> with all relevant Yoga Alliance&nbsp;credentials, including all qualifications and corresponding continuing education requirements;</li>
	<li><strong>Agree to follow</strong> all other Yoga Alliance policies and procedures.</li>
</ol>
