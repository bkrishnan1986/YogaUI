<h2>Purpose </h2>
<p>Yoga Alliance™ and the Yoga Alliance Foundation™ envision a world in which access to high quality teaching and practicing of yoga is equitable for all and elevates collective wellbeing and human consciousness. The purpose of the SOP and the overall Ethical Commitment is to foster safe and respectful guidelines for the <a href="https://devteam.yogaalliance.org/About_Us/Policies/Definition_of_Key_Terms#business-of-yoga" target="_blank">Profession of yoga</a> and to assure that Yoga Alliance Members maintain a high level of teaching ability and accountability.
</p>
<p>By defining and requiring high standards of professional and ethical conduct, Yoga Alliance seeks to promote confidence and respect for Members throughout yoga communities and the public at large.  This includes: (a) broadening the public understanding of the role of the RYT; (b) ensuring and upholding professional behavior within the Yoga Alliance community; (c) developing and encouraging high standards of professional conduct; and (d) promoting and protecting the interests of the profession of yoga teaching and of the Yoga Alliance members we serve.
</p>
<br>
<h2>Preamble</h2>
<p>Yoga Alliance™’s SOP provides a set of robust standards, policies, and procedures for the profession of yoga teaching. Towards that end, Yoga Alliance recognizes that there are many lineages, styles, and methodologies and believes that the teaching of yoga must respect that diversity.  Yoga is a polythetic system<sup>1</sup> of physical, mental, and spiritual practices and disciplines which originated in ancient India and South Asia. Throughout history, various groups in India, and in other cultures, geographies, and religious and secular contexts, have defined and redefined yoga according to the differing circumstances, passions, and goals of diverse individual and cultural traditions. The colonization of India by Great Britain, the globalization of yogic understandings, and a combination of other worldwide cultural exchanges, appropriations, and natural evolutions have also influenced what is defined today as yoga. While acknowledging the limitations of any single definition of yoga—and wholly rejecting the idea that yoga can be reduced to any single framework or understanding—for the purpose of Yoga Alliance standards, the Sanskrit word “yoga” includes and additionally describes both an optimal unitive state of consciousness as well as the techniques, philosophies, practices, and lifestyles that bring one to such a state
</p>
<p>Yoga Alliance Members make a commitment to high standards of competency and ethics; this SOP is part of that commitment and provides a non-lineage specific description of yoga teaching for all Members.
</p>
<p>This Scope is not intended to limit a licensed healthcare practitioner or certified professional from practicing according to their respective Scopes of Practice.
</p>
