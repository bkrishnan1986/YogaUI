<h2>Introduction</h2>
<p><a href="https://devteam.yogaalliance.org/About_Us/Policies/Definition_of_Key_Terms#yoga-alliance" target="_blank">Yoga Alliance™’s</a> Scope of Practice (SOP) applies to every Yoga Alliance <a href="https://devteam.yogaalliance.org/About_Us/Policies/Definition_of_Key_Terms#member" target="_blank">Member</a>. The SOP provides a clear, responsible, and non-lineage-specific description of yoga teaching, practice standards, and professional responsibilities for all Yoga Alliance Members of any kind (now known or unknown), including the Yoga Alliance Registered Yoga <a href="https://devteam.yogaalliance.org/About_Us/Policies/Definition_of_Key_Terms#teacher" target="_blank">Teacher</a> (RYT™), Continuing Education Provider (YACEP®), Yoga Teacher Specialist (RCYT™, RPYT™, etc.), and others. The SOP sets out acceptable, recommended, required, and prohibited practices by which Members agree to conduct themselves.  The Member must adhere to the SOP while teaching yoga. It is based upon core values intrinsic to the practice of yoga.
</p>
<p>The SOP is an integral part of the overall <a href="https://devteam.yogaalliance.org/New_RYS_Standards/The_Ethical_Commitment" target="_blank">Ethical Commitment</a> to which a Member must agree, along with <a href="https://devteam.yogaalliance.org/AboutYA/OurPolicies/CodeofConduct" target="_blank">Yoga Alliance’s Code of Conduct</a> and all <a href="https://devteam.yogaalliance.org/About_Us/Policies" target="_blank">other Yoga Alliance policies</a>.
</p>
<p>Terms in bold are considered key terms and are defined <a href="https://devteam.yogaalliance.org/About_Us/Policies/Definition_of_Key_Terms" target="_blank">here</a><a></a>.
